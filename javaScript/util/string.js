//***export 선언부분************************************************************************************* */
export {
    pad, //원하는 글자에 0 포함시키는 함수
};



//***함수 선언부분************************************************************************************* */
function pad(n, width) {
    n = n + '';
    return n.length >= width ? n : new Array(width - n.length + 1).join('0') + n;
}